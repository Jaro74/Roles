    private void cargarTabla() {
        //1 definir el modelo que queremos de tabla 
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            
            // que fila y columnas no sean editables
            @Override
            public boolean isCellEditable (int row, int column){
                return false;
        }
        };
        String titulos [] = {"Id", "Usuario", "Rol"};
        //2 asignar los titulos 
        modeloTabla.setColumnIdentifiers(titulos);
        // 4 cargar con datos, traer los usuarios de la base de datos la lista
        List <Usuario> listaUsuario = control.traerUsuario();
        //preguntamos si la lista esta vacia
        if (listaUsuario !=null){
            
            //recorrer la lista
            for (Usuario usu : listaUsuario){
                Object [] objeto = {usu.getId(), usu.getNombreUsuario(), usu.getUnRol().getNombreRol()};
                
                //agregar una fila a la tabla
                modeloTabla.addRow(objeto);
            }
            
        }
        
        
        // 3 asignar a la tabla
        tablaUsuarios.setModel(modeloTabla);
        
        
    }//GEN-LAST:event_formWindowOpened

