    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        
        //la tabada tenga elementos
        if (tablaUsuarios.getRowCount()> 0){
            //que se haya seleccionado un elemento si es -1 los elementos empiezan en cero
            // por tanto si es -1 es que no hay ninguno 
            if (tablaUsuarios.getSelectedRow() != -1){
                //id del usuario la primer columna
                // doble conversi√≥n espera int y es obj primero transformo en String y despu√©s a int
                int id_usuario = Integer.parseInt(String.valueOf(tablaUsuarios.getValueAt(tablaUsuarios.getSelectedRow(),0)));
                
                //llamo al metodo borrar. estoy IGU llamo controladora
                control.borrarUsuario(id_usuario);
                // avisar el usuario el borrado
                
                mostrarMensaje("Se borro el usuario correctamente", "Info", "Eliminaci√≥n correcta");
                cargarTabla();
            }
            else {
                mostrarMensaje("No seleccion√≥ ning√∫n registro", "Error", "Error al borrar");
            }
            
        
        }
        else{
            mostrarMensaje("La tabla esta vacia", "Error", "Error al borrar");
        }
        
    
        
    }//GEN-LAST:event_btnBorrarActionPerformed

